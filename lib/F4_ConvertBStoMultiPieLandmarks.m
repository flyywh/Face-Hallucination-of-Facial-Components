function landmarks = F4_ConvertBStoMultiPieLandmarks(bs)
    %compute the block center
    centerxy = zeros(68,2);
    for i=1:68
        centerxy(i,1) = (bs.xy(i,1) + bs.xy(i,3))/2;
        centerxy(i,2) = (bs.xy(i,2) + bs.xy(i,4))/2;
    end
    
    %re-order as the Multi-Pie format
    mappingtable = ...
       [60 59 58 57 56 55 54 53 52 ...
        61 62 63 64 65 66 67 68 ...
        16 17 18 19 20 ...
        31 30 29 28 27 ...
        9 8 7 6 ...
        3 2 1 4 5 ...
        15 14 13 10 11 12 ...
        21 24 25 26 23 22 ...
        35 34 33 32 39 40 41 44 46 51 48 50 36 37 38 43 42 45 47 49];
    landmarks = centerxy(mappingtable, :);
end